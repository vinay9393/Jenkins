#!/bin/bash

# Command 1: Print the current date and time
date

# Command 2: List files in the current directory
ls

# Command 3: Create a new directory
mkdir my_directory

# Command 4: Change to the new directory
cd my_directory

# Command 5: Create a new file
touch my_file.txt

# Command 6: Add some text to the file
echo "Hello, World!" > my_file.txt

# Command 7: Display the contents of the file
cat my_file.txt

# Command 8: Remove the file and the directory
rm my_file.txt
cd ..
rmdir my_directory

